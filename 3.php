<?php
$x1 = rand(-100, 100);
$x2 = rand(-100, 100);
if($x1 >= 0 && $x2 >= 0) {
    echo '$x1 = ' . $x1 . ' $x2 = ' . $x2 . ' Обе переменные положительные.';
}elseif($x1 < 0 && $x2 < 0) {
    echo '$x1 = ' . $x1 . ' $x2 = ' . $x2 . ' Обе переменные отрицательные.';
}elseif($x1 < 0 && $x2 > 0 || $x1 > 0 && $x2 < 0) {
    echo '$x1 = ' . $x1 . ' $x2 = ' . $x2 . ' Переменные имеют разные знаки.';
}
